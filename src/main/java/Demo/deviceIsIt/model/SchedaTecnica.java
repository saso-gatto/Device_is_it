package Demo.deviceIsIt.model;

public class SchedaTecnica {
	
	private int idScheda;
	private String cpu;
	private String ram;
	private String display;
	private String batteria;
	private String fotocamera;
	private String memoria;
	
	
	public int getIdScheda() {
		return idScheda;
	}
	public void setIdScheda(int idScheda) {
		this.idScheda = idScheda;
	}
	public String getCpu() {
		return cpu;
	}
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}
	public String getRam() {
		return ram;
	}
	public void setRam(String ram) {
		this.ram = ram;
	}
	public String getDisplay() {
		return display;
	}
	public void setDisplay(String display) {
		this.display = display;
	}
	public String getBatteria() {
		return batteria;
	}
	public void setBatteria(String batteria) {
		this.batteria = batteria;
	}
	public String getFotocamera() {
		return fotocamera;
	}
	public void setFotocamera(String fotocamera) {
		this.fotocamera = fotocamera;
	}
	public String getMemoria() {
		return memoria;
	}
	public void setMemoria(String memoria) {
		this.memoria = memoria;
	}
	
	

}
