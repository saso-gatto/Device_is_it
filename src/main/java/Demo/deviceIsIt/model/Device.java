package Demo.deviceIsIt.model;

public class Device {
	
	private int idDevice;
	private String modello;
	private String marca;
	private int tipoDevice;
	private String memoria;
	private String ram;
	private String display;
	private String batteria;
	private String fotocamera;
	private String cpu;
	private String peso;
	private String os;
	private String img;
	
	
	public int getIdDevice() {
		return idDevice;
	}
	public void setIdDevice(int idDevice) {
		this.idDevice = idDevice;
	}
	public String getModello() {
		return modello;
	}
	public void setModello(String modello) {
		this.modello = modello;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public int getTipoDevice() {
		return tipoDevice;
	}
	public void setTipoDevice(int tipoDevice) {
		this.tipoDevice = tipoDevice;
	}
	public String getCPU() {
		return cpu;
	}
	public void setCPU(String cpu) {
		this.cpu = cpu;
	}
	public String getRam() {
		return ram;
	}
	public void setRam(String ram) {
		this.ram = ram;
	}
	public String getDisplay() {
		return display;
	}
	public void setDisplay(String display) {
		this.display = display;
	}
	public String getBatteria() {
		return batteria;
	}
	public void setBatteria(String batteria) {
		this.batteria = batteria;
	}
	public String getFotocamera() {
		return fotocamera;
	}
	public void setFotocamera(String fotocamera) {
		this.fotocamera = fotocamera;
	}
	public String getMemoria() {
		return memoria;
	}
	public void setMemoria(String memoria) {
		this.memoria = memoria;
	}
	public String getPeso() {
		return peso;
	}
	public void setPeso(String peso) {
		this.peso = peso;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	

	
}
